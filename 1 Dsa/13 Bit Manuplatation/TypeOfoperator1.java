public class TypeOfoperator1{

public static void main (String[] args ){
System.out.println(" And operater");
System.out.println(6&5);


System.out.println(" Or operater");
System.out.println(6|5);




System.out.println(" xOr operater");
System.out.println(6^5);





System.out.println(" One,s   (not)  operater");
System.out.println(~5);



System.out.println(" One,s   (not)  operater on zero");
System.out.println(~0);



System.out.println(" Binary left shift ");
System.out.println((5<<1));


System.out.println(" Binary right shift ");
System.out.println((5>>1));




}




}